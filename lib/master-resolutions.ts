export const AUTH =
  "\n\nRESOLVED FURTHER THAT any Director / Company Secretary of the Company be and is hereby severally authorized to do all such acts, deeds, matters and things as may be considered necessary, proper or expedient to give effect to this resolution.";

export function fillMasterTemplate(text: string, fields: Record<string, string>): string {
  return text.replace(/\{(\w+)\}/g, (_, key) => fields[key] ?? `{${key}}`);
}

function noting(subject: string): string {
  return `The Board noted and took on record ${subject}.`;
}

function approving(subject: string): string {
  return `The Board approved ${subject} as presented.`;
}

