// lib/master-resolutions.ts
// Single source of truth for ALL resolutions used across Board Minutes, Board Resolution Builder, AGM Minutes, Sub-Committee Minutes

export interface MasterField {
  key: string;
  label: string;
  placeholder?: string;
  type?: "text" | "date" | "number" | "textarea" | "select";
  options?: string[];
  required?: boolean;
}

export interface MasterResolution {
  id: string;
  title: string;
  agendaTitle?: string;
  icon: string;
  category: string;
  meetingType: "board" | "agm" | "egm" | "subcommittee";
  kind: "ordinary" | "special" | "none";
  section?: string;
  rocFiling?: string;
  resolutionLaw?: string;
  proposal?: string;
  discussion: string;
  preamble?: string;
  resolution: string;
  fields: MasterField[];
  notes?: string;
}

